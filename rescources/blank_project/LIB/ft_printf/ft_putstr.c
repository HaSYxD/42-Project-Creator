#include "ft_printf.h"

void	ft_putstr(const char *str, int *i)
{
	if (!str)
	{
		write(1, "(null)", 6);
		*i += 6;
		return ;
	}
	while (*str)
	{
		write(1, &*str++, 1);
		++*i;
	}
}
